let day=Number(prompt("enter day"));
switch(day){
    case 1:
        alert("Sunday");
        break;
    
    case 2:
        alert("Monday");
        break;
    
    case 3:
        alert("Tuesday");
        break;
    
    case 4:
        alert("Wednesday");
        break;
    
    case 5:
        alert("Thrusday");
        break;
    
    case 6:
        alert("Friday");
        break;
    
    case 7:
        alert("Saturday");
        break;
    
    default:
        alert("Invalid")
        break;
    }
